package com.example.fab083.dtos;

public class DtoObtenerHoraSistemaInput {
    public int horaSistema ;
    public int minutoSistema ;
    public int segundoSistema ;

    public int getHoraSistema() {
        return horaSistema;
    }

    public void setHoraSistema(int horaSistema) {
        this.horaSistema = horaSistema;
    }

    public int getMinutoSistema() {
        return minutoSistema;
    }

    public void setMinutoSistema(int minutoSistema) {
        this.minutoSistema = minutoSistema;
    }

    public int getSegundoSistema() {
        return segundoSistema;
    }

    public void setSegundoSistema(int segundoSistema) {
        this.segundoSistema = segundoSistema;
    }
}
